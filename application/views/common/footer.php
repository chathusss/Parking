            $("#out_time").show();
            $('#print').show();
            $.post("<?php echo base_url(); ?>index.php/Vehicle/FindVehicle", {card_no: card_no}).done(function (data) {
                if (data.length === 2) {

                    swal({
                        title: "No Data Found !",
                        text: "No Vehicle Found For Following Card Number",
                        timer: 20000,
                        showConfirmButton: true
                    });

                } else {
                    var jsonObject = JSON.parse(data)
                    $.each(jsonObject, function (index, value) {
                        $('#vehicle').val(value.Vehicle_Number);
                        $('#card_no').val(value.Vehicle_Card_Number);
                        $("#in_time").val(value.Vehicle_IN_Time);
                        $("#out_time").val(value.Vehicle_OUT_Time);
                        $("#nic").val(value.Owner_NIC);
                        if (value.Vehicle_Type === "Two Wheeler") {
                            $("#two").prop("checked", true);
                            $("#three").prop("checked", false);
                            $("#four").prop("checked", false);
                        } else if (value.Vehicle_Type === "Three Wheeler") {
                            $("#three").prop("checked", true);
                            $("#two").prop("checked", false);
                            $("#four").prop("checked", false);
                        } else if (value.Vehicle_Type === "Four Wheeler") {
                            $("#four").prop("checked", true);
                            $("#two").prop("checked", false);
                            $("#three").prop("checked", false);
                        } else {
                            $("#four").prop("checked", false);
                            $("#two").prop("checked", false);
                            $("#three").prop("checked", false);
                        }
                    });
                }
            });
        }
    }



