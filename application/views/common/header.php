                function ResetPassword() {
                    var user = $('#user').val();
                    var oldpass = $('#oldpass').val();
                    var newpass = $('#newpass').val();
                    var newpass2 = $('#newpass2').val();
                    if (user === "" || user === null) {
                        $('#user').focus();
                        $('#r_user').html("User Name Required");
                        return false;
                    } else if (oldpass === "" || oldpass === null) {
                        $('#oldpass').focus();
                        $('#Old_p').html("Old Password Required");
                        return false;
                    } else if (newpass === "" || newpass === null) {
                        $('#newpass').focus();
                        $('#new_p').html("New Password Required");
                        return false;
                    } else if (newpass2 === "" || newpass2 === null) {
                        $('#newpass2').focus();
                        $('#confirm').html("Confrirm Password  Required");
                        return false;
                    } else if (newpass2 !== newpass) {
                        $('#newpass2').focus();
                        $('#confirm').html("Password Not Same");
                        return false;
                    } else {
                        $.post("<?php echo base_url(); ?>index.php/User/RestPassword", {user: user, oldpass: oldpass, newpass: newpass}).done(function (data) {
                            
                                swal("Reset Successfull!", "Successfully Reset Your Password", "success");
                         

                            location.reload();
                        });
                    }


                }
            </script>